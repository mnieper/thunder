#ifdef HAVE_CONFIG_H
# include <config.h>
#endif

#include "array.h"

size_t array_hash (size_t const *entry, size_t n)
{
  return entry % n;
}

bool array_compare (size_t const *entry1, size_t const *entry2)
{
  return entry1 == entry2;
}
